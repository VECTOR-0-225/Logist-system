import sqlite3

DB_NAME = 'logistics_network.db'

def init_db():
    """初始化数据库，创建商家表"""
    conn = sqlite3.connect(DB_NAME)
    cursor = conn.cursor()
    cursor.execute('''
        CREATE TABLE IF NOT EXISTS merchants (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            name TEXT UNIQUE NOT NULL,
            lng REAL NOT NULL,
            lat REAL NOT NULL
        )
    ''')
    conn.commit()
    conn.close()

def add_merchant(name, lng, lat):
    """新增或更新商家坐标"""
    conn = sqlite3.connect(DB_NAME)
    cursor = conn.cursor()
    try:
        cursor.execute('INSERT OR REPLACE INTO merchants (name, lng, lat) VALUES (?, ?, ?)', (name, lng, lat))
        conn.commit()
        print(f"✅ 商家 [{name}] 坐标已成功同步至数据库。")
    except Exception as e:
        print(f"❌ 💡 插入失败: {e}")
    finally:
        conn.close()

def delete_merchant(name):
    """根据名字删除商家"""
    conn = sqlite3.connect(DB_NAME)
    cursor = conn.cursor()
    cursor.execute('DELETE FROM merchants WHERE name = ?', (name,))
    if cursor.rowcount > 0:
        print(f"🗑️ 商家 [{name}] 已从库中彻底删除。")
    else:
        print(f"⚠️ 库中未找到名为 [{name}] 的商家。")
    conn.commit()
    conn.close()

def show_all_merchants():
    """查看库中所有商家"""
    conn = sqlite3.connect(DB_NAME)
    cursor = conn.cursor()
    cursor.execute('SELECT name, lng, lat FROM merchants')
    rows = cursor.fetchall()
    conn.close()
    print("\n📋 当前数据库中存储的商家列表：")
    print("-" * 50)
    for row in rows:
        print(f"· 店名: {row[0]:<15} | 经度: {row[1]:.6f} | 纬度: {row[2]:.6f}")
    print("-" * 50)
    return rows

